# Sort by length descending so longer phrases match first
_SORTED_TRANSLATIONS: list[tuple[str, str]] = sorted(
    _TRANSLATIONS.items(), key=lambda kv: len(kv[0]), reverse=True
)


def set_language(lang: str) -> None:
    """Set the active language ('zh' or 'en')."""
    global _LANG
    _LANG = lang.lower()


def get_language() -> str:
    """Return the current language code."""
    return _LANG


def translate(text: str) -> str:
    """
    Replace every Chinese substring in *text* with its English equivalent.
    Non-Chinese parts (variable values, numbers, paths) are preserved as-is.
    """
    if _LANG == "zh":
        return text

    # Use a null-byte marker so we can detect translation boundaries and
    # add spaces wherever a translated segment meets a Python variable.
    _M = "\x00"
    result = text
    for zh, en in _SORTED_TRANSLATIONS:
        if zh in result:
            result = result.replace(zh, f"{_M}{en}{_M}")

    # Ensure a space separates a translated segment from adjacent word chars
    result = re.sub(r"([a-zA-Z0-9])\x00", r"\1 ", result)
    result = re.sub(r"\x00([a-zA-Z0-9])", r" \1", result)
    result = result.replace(_M, "")

    # Replace any remaining Chinese characters with a readable placeholder
    result = _CHINESE_RE.sub(
        lambda m: f"[{m.group()[:6]}…]" if len(m.group()) > 6 else f"[{m.group()}]",
        result,
    )

    # Fix spacing from camelCase joins (e.g. "pilotExperiment" → "pilot Experiment")
    result = re.sub(r"(?<=[a-z])(?=[A-Z])", " ", result)
    # Collapse any accidental double-spaces
    result = re.sub(r"  +", " ", result)
    return result


class TranslatingFormatter(logging.Formatter):
    """
    A logging.Formatter that runs translate() on every log message when the
    language is set to 'en'.  Install it once on the root logger and every
    handler will emit English-language output automatically.
    """

    def __init__(self, fmt: Optional[str] = None, datefmt: Optional[str] = None):
        super().__init__(fmt=fmt, datefmt=datefmt)

    def format(self, record: logging.LogRecord) -> str:
        original = super().format(record)
        return translate(original)


def install_translating_formatter(
    fmt: str = "%(asctime)s - %(name)s - %(levelname)s - %(message)s",
    datefmt: Optional[str] = None,
) -> None:
    """
    Replace the formatter on every existing handler attached to the root logger
    with a TranslatingFormatter using the given format string.
    Call this *after* basicConfig() has been called so handlers already exist.
    """
    formatter = TranslatingFormatter(fmt=fmt, datefmt=datefmt)
    root = logging.getLogger()
    for handler in root.handlers:
        handler.setFormatter(formatter)
